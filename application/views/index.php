            $("#submit").click(function(e){

                var name    = $('#name').val().trim();
                var mobile  = $('#mobile').val().trim();
                var email   = $('#email').val().trim().toLowerCase();
                var message = $('#message').val().trim();

                if (name == "") {
                    swal("Name is required","Please don't waste your precious time by not entering the name!","error");
                    return false;
                }else{
                    var temp_name = name.split(' ');
                    var short_name = temp_name[0];
                }
                
                if (mobile == "") {
                    swal(short_name+", Mobile is required","Please don't waste your precious time by not entering the mobile!","error");
                    return false;
                }    
                
                if (email == "") {
                    swal(short_name+", Email is required","Please don't waste your precious time by not entering the email!","error");
                    return false;
                }
                
                if (message == "") {
                    swal(short_name+", Message is required","Please don't waste your precious time by not entering the message!","error");
                    return false;
                }

                e.preventDefault();

                $.ajax({
                    type : "POST",
                    dataType : "html",
                    url : 'http://mdbhoraniya.esy.es/welcome/save_data',
                    data : ({
                        name : name,
                        mobile : mobile,
                        email : email,
                        message : message
                    }),
                    success: function(data){
                        if(data==1){
                            swal({
                              title: "Thank you, "+short_name,
                              text: "I have received your enquiry and will get back to you soon!",
                              icon: "success",
                            })
                            .then((clear) => {
                                if (clear) {
                                    $('#name').val('');
                                    $('#mobile').val('');
                                    $('#email').val('');
                                    $('#message').val('');
                                } 
                            });
                        }else{
                            swal({
                              title: "Error!",
                              text: "The form was not submitted, Please try again!",
                              icon: "error",
                            })
                            .then((clear) => {
                                if (clear) {
                                    location.reload();
                                } 
                            });
                        }                  
                       
                    },
                    beforeSend : function(){
                        // swal("Submitting data");
                    }
                });    

            });
        });
